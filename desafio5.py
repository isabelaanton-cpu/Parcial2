# Solicita a medida da base do triângulo
base = float(input("Digite o valor da base do triângulo: ")) 
# Solicita a medida da altura do triângulo
altura = float(input("Digite o valor da altura do triângulo: ")) 
# Aplica a fórmula da área: (base * altura) / 2
area = (base * altura) / 2 
# Exibe o valor da área calculado
print(f"A área do triângulo é: {area}")
