# Solicita o primeiro número ao usuário e converte para real (float)
num1 = float(input("Digite o primeiro número: ")) 
# Solicita o segundo número ao usuário e converte para real (float)
num2 = float(input("Digite o segundo número: ")) 
# Realiza a soma dos dois valores informados
